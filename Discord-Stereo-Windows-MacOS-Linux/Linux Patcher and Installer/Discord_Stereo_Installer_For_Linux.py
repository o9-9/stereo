def _cache_dir():
    from pathlib import Path
    d = Path.home() / ".cache" / "DiscordVoiceFixerStandalone"
    d.mkdir(parents=True, exist_ok=True)
    return d

def _extract(name, b64_blob):
    from pathlib import Path
    cache = _cache_dir()
    out = cache / name
    raw = base64.b64decode("".join(b64_blob.split()))
    data = zlib.decompress(raw)
    if not out.is_file() or out.read_bytes() != data:
        out.write_bytes(data)
    out.chmod(0o755)
    return out

